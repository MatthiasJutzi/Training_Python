# upper()
myStr1 = 'Hello World'
uppercase_myStr1 = myStr1.upper()
print(uppercase_myStr1)

# lower()
myStr2 = 'Hello World'
lowercase_myStr2 = myStr2.lower()
print(lowercase_myStr2)

# strip()
myStr3 = ' hello world '
stripmyStr3 = myStr3.strip()
print(stripmyStr3)

# replace(old, new)
myStr4 = 'hello world'
replacemyStr4 = myStr4.replace('hello', 'hi')
print(replacemyStr4)

# split()
myStr5 = 'hello world'
splitmyStr5 = myStr5.split()
print(splitmyStr5)

# join()
myList1 = ['hello', 'world']
joinmyList1 = ' '.join(myList1)
print(joinmyList1)

# startwith(prefix)
myStr6 = 'hello world'
startwithmyStr6 = myStr6.stratwith('hello')
print(startwithmyStr6)

# enwith(suffix)
myStr7 = 'hello world'
endwithmyStr7 = myStr7.endwith('world')
print(endwithmyStr7)

# find(substring)
myStr8 = 'hello world'
findmyStr8 = myStr8.find('world')
print(findmyStr8)

# count(substring)
myStr9 = 'hello world'
countmyStr9 = myStr9.count('o')
print(countmyStr9)



